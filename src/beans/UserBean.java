package beans;

/**
 * @author Srikesh Mandala(smandal2@exchange.asu.edu)
 *
 * The UserBean is a bean class consisting of 
 * three members isUserRegistered,userId
 * & isUserValid
 */
public class UserBean {

	private int isUserRegistered;
	private String userId;
	private boolean isUserValid;

	public boolean isUserValid() {
		return isUserValid;
	}

	public void setUserValid(boolean isUserValid) {
		this.isUserValid = isUserValid;
	}

	public int getIsUserRegistered() {
		return isUserRegistered;
	}

	public void setIsUserRegistered(int isUserRegistered) {
		this.isUserRegistered = isUserRegistered;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

}
