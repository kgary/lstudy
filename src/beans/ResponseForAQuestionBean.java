package beans;

import java.io.Serializable;
import java.util.ArrayList;

@SuppressWarnings("serial")
public class ResponseForAQuestionBean implements Serializable {
	
	private String questionId;
	private String userid;
	private String response;
	private ArrayList<SubQuestionsBean> subQuestions;
	
	public String getQuestionId() {
		return questionId;
	}
	public void setQuestionId(String questionId) {
		this.questionId = questionId;
	}
	public ArrayList<SubQuestionsBean> getSubQuestions() {
		return subQuestions;
	}
	public void setSubQuestions(ArrayList<SubQuestionsBean> subQuestions) {
		this.subQuestions = subQuestions;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getResponse() {
		return response;
	}
	public void setResponse(String response) {
		this.response = response;
	}
	

}
